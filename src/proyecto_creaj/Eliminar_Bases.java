    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        try{//Inicio del try
        //Creacion de objetro conexion    
        Conexion conexion1 =new Conexion();
        //Variable que guarda el query para eliminar bases de datos
        String SqlEliminar;
        //Asignamos el query a la variable
        SqlEliminar="drop database "+cmbBases.getSelectedItem().toString();
        //Ejecutamos el metodo del objeto conexion que ejecuta la consulta por medio del objeto stament de la clase
        conexion1.setQuery(SqlEliminar);
        //Mostramos mensaje
        JOptionPane.showMessageDialog(null, "Base de datos "+cmbBases.getSelectedItem().toString()+" Eliminada Exitosamente");
        //Remuevo la base del ComboBox
        cmbBases.removeItem(cmbBases.getSelectedItem());
        }//Fin del try
        
        catch(Exception ex){//Inicio del catch
          JOptionPane.showMessageDialog(null, ex,"Error",JOptionPane.ERROR_MESSAGE);
        }//Fin del catch
    }//GEN-LAST:event_btnEliminarActionPerformed

